package clientes;

public class Direccion {

	private String calle;
	private String altrua;
	private String piso;
	private String dto;

	
	public String getCalle() {
		return calle;
	}
	public void setCalle(String calle) {
		this.calle = calle;
	}
	public String getAltrua() {
		return altrua;
	}
	public void setAltrua(String altrua) {
		this.altrua = altrua;
	}
	public String getPiso() {
		return piso;
	}
	public void setPiso(String piso) {
		this.piso = piso;
	}
	public String getDto() {
		return dto;
	}
	public void setDto(String dto) {
		this.dto = dto;
	}
	
	



}
